/**
 * Safely seed SEO fields (metaTitle, metaDescription) on Sanity documents.
 *
 * Guarantees — by design, never clobbers server edits:
 *   - Uses patch().setIfMissing() at the FIELD level (not document level)
 *   - Any SEO field a human has already edited in Studio is SKIPPED
 *   - Missing documents are logged as SKIP, not errors
 *   - No createOrReplace, no set() — only setIfMissing()
 *
 * Pre-flight:
 *   Before any writes, prints the current SEO state of every target
 *   document so you can see exactly what is about to change.
 *
 * Run:
 *   cd www
 *   set -a && source .env.local && set +a && pnpm tsx scripts/seed-seo.ts
 *
 * Copy source:
 *   Generated by a 3-agent brand/SEO council (see Plans/seo-seed-prd.md).
 *   To rewrite, edit COPY below and re-run. Fields a human has since
 *   edited in Studio will NOT be overwritten — clear them in Studio first.
 */
import { createClient, type SanityClient } from '@sanity/client';

const client: SanityClient = createClient({
  projectId: process.env.NEXT_PUBLIC_SANITY_PROJECT_ID,
  dataset: process.env.NEXT_PUBLIC_SANITY_DATASET || 'production',
  apiVersion: '2025-01-01',
  useCdn: false,
  token: process.env.SANITY_WRITE_TOKEN,
});

