type TargetDoc = {
  id: string;
  label: string;
  copy: SeoCopy;
};

type ExistingSeo = { metaTitle?: string; metaDescription?: string } | null;

function truncate(value: string | undefined, max = 80): string {
  if (!value) return '<empty>';
  return value.length > max ? `${value.slice(0, max - 1)}…` : value;
}

async function resolveTargets(): Promise<TargetDoc[]> {
  // Singletons + readings: _id IS the COPY key. Readings get a friendlier label.
  const targets: TargetDoc[] = Object.entries(COPY).map(([id, copy]) => ({
    id,
    label: id.startsWith('reading-')
      ? `reading(${id.slice('reading-'.length)})`
      : id,
    copy,
  }));

  // Resolve legalPage _ids by slug.
  const legalSlugs = Object.keys(LEGAL_COPY);
  const legalDocs = await client.fetch<
    Array<{ _id: string; slug: { current: string } }>
  >(
    `*[_type == "legalPage" && slug.current in $slugs]{ _id, slug }`,
    { slugs: legalSlugs },
  );
  for (const slug of legalSlugs) {
    const doc = legalDocs.find((d) => d.slug?.current === slug);
    if (doc) {
      targets.push({
        id: doc._id,
        label: `legalPage(${slug})`,
        copy: LEGAL_COPY[slug as keyof typeof LEGAL_COPY],
      });
    } else {
      console.warn(
        `WARN  legalPage with slug "${slug}" not found on server — will be skipped.`,
      );
    }
  }

  return targets;
}

async function preflight(targets: TargetDoc[]): Promise<Map<string, ExistingSeo>> {
  const ids = targets.map((t) => t.id);
  const docs = await client.fetch<
    Array<{ _id: string; seo?: { metaTitle?: string; metaDescription?: string } }>
  >(`*[_id in $ids]{ _id, seo }`, { ids });

  const state = new Map<string, ExistingSeo>();
  console.log('=== Pre-flight: current SEO state ===');
  for (const t of targets) {
    const doc = docs.find((d) => d._id === t.id);
    if (!doc) {
      console.log(`  ${t.label} (${t.id}): document not found on server`);
      state.set(t.id, null);
    } else {
      const seo = doc.seo ?? {};
      state.set(t.id, seo);
      console.log(
        `  ${t.label} (${t.id}): metaTitle=${truncate(seo.metaTitle)}, metaDescription=${truncate(seo.metaDescription)}`,
      );
    }
  }
  console.log('');
  return state;
}

async function seedTarget(
  target: TargetDoc,
  existing: ExistingSeo,
): Promise<{ filled: number; skipped: number }> {
  if (existing === null) {
    console.log(`SKIP  ${target.label}: document missing on server`);
    return { filled: 0, skipped: 2 };
  }

  let filled = 0;
  let skipped = 0;

  // Patch builder: ensure seo object exists, then setIfMissing each field.
  // setIfMissing is idempotent and will only write fields that have no value.
  const patch = client
    .patch(target.id)
    .setIfMissing({ seo: {} })
    .setIfMissing({
      'seo.metaTitle': target.copy.metaTitle,
      'seo.metaDescription': target.copy.metaDescription,
    });

  try {
    await patch.commit();
  } catch (err) {
    console.error(`ERROR ${target.label}: patch failed —`, err);
    throw err;
  }

  // Report per-field outcome based on preflight state.
  if (existing.metaTitle) {
    console.log(`SKIP  ${target.label}.seo.metaTitle (already set)`);
    skipped++;
  } else {
    console.log(`FILL  ${target.label}.seo.metaTitle`);
    filled++;
  }
  if (existing.metaDescription) {
    console.log(`SKIP  ${target.label}.seo.metaDescription (already set)`);
    skipped++;
  } else {
    console.log(`FILL  ${target.label}.seo.metaDescription`);
    filled++;
  }

  return { filled, skipped };
}

async function main(): Promise<void> {
  if (!process.env.NEXT_PUBLIC_SANITY_PROJECT_ID) {
    throw new Error('NEXT_PUBLIC_SANITY_PROJECT_ID is required');
  }
  if (!process.env.SANITY_WRITE_TOKEN) {
    throw new Error('SANITY_WRITE_TOKEN is required');
  }

  const targets = await resolveTargets();
  const state = await preflight(targets);

  console.log('=== Patching ===');
  let totalFilled = 0;
  let totalSkipped = 0;
  for (const target of targets) {
    const res = await seedTarget(target, state.get(target.id) ?? null);
    totalFilled += res.filled;
    totalSkipped += res.skipped;
  }

  console.log('');
  console.log(`Done. Filled ${totalFilled}, skipped ${totalSkipped}.`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
